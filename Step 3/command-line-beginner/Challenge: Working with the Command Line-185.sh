## 1. Command Line Python ##

~$ pwd

## 2. Creating a script ##

~$ nano script.py

## 3. Change file permissions ##

~$ chmod 0700 script.py

## 4. Create a virtualenv ##

~$ source script/bin/activate

## 5. Move the script ##

(script) ~$ mv script.py printer/

## 6. Execute the script ##

(script) ~/printer$ python script.py "I'm so good at challenges!"